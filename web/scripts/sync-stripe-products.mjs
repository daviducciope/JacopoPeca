#!/usr/bin/env node
/**
 * Sync WooCommerce catalog products → Stripe Products + Prices.
 * Run once: node scripts/sync-stripe-products.mjs
 *
 * Requires: STRIPE_SECRET_KEY in the environment.
 * Outputs: src/lib/stripe-prices.ts (priceId per product slug)
 */

import { readFileSync, writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const WEB_DIR = resolve(__dirname, "..");

const SK = process.env.STRIPE_SECRET_KEY;

if (!SK) {
  console.error("Missing STRIPE_SECRET_KEY environment variable");
  process.exit(1);
}

async function stripePost(endpoint, params) {
  const body = new URLSearchParams();
  for (const [k, v] of Object.entries(params)) {
    if (v !== undefined && v !== null) body.append(k, String(v));
  }
  const res = await fetch(`https://api.stripe.com/v1${endpoint}`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${SK}`,
      "Content-Type": "application/x-www-form-urlencoded",
    },
    body: body.toString(),
  });
  const data = await res.json();
  if (data.error) throw new Error(`Stripe ${endpoint}: ${data.error.message}`);
  return data;
}

// Load catalog
const catalogSrc = readFileSync(resolve(WEB_DIR, "src/lib/catalog.ts"), "utf8");
const productsMatch = catalogSrc.match(/export const products: CatalogProduct\[\] = (\[[\s\S]*?\n\]);/);
if (!productsMatch) { console.error("Cannot parse products from catalog.ts"); process.exit(1); }

// Use Function constructor to eval the array safely
const products = new Function(`return ${productsMatch[1]}`)();
console.log(`Found ${products.length} products in catalog`);

const priceMap = {};
let created = 0;

for (const p of products) {
  if (!p.priceCents || p.priceCents < 50) {
    console.log(`  SKIP ${p.slug} (price ${p.priceCents}c)`);
    continue;
  }

  try {
    // Create Stripe product
    const imgUrl = p.images?.[0]?.url;
    const productParams = {
      name: p.name,
      "metadata[wc_id]": p.id,
      "metadata[slug]": p.slug,
      "metadata[category]": p.categoryName || "",
    };
    if (imgUrl) productParams["images[0]"] = imgUrl;

    const stripeProduct = await stripePost("/products", productParams);

    // Create one-time price
    const price = await stripePost("/prices", {
      product: stripeProduct.id,
      currency: "eur",
      unit_amount: p.priceCents,
    });

    priceMap[p.slug] = price.id;
    created++;
    console.log(`  ✓ ${p.slug} → ${price.id} (${p.priceCents}c)`);
  } catch (err) {
    console.error(`  ✗ ${p.slug}: ${err.message}`);
  }

  // Rate limiting - Stripe allows 25 req/s in test, 100 in live
  await new Promise((r) => setTimeout(r, 120));
}

// Generate TypeScript file
const ts = `// Auto-generated by sync-stripe-products.mjs — do not edit
// Maps product slug → Stripe Price ID for client-side checkout

export const stripePrices: Record<string, string> = ${JSON.stringify(priceMap, null, 2)};
`;

writeFileSync(resolve(WEB_DIR, "src/lib/stripe-prices.ts"), ts);
console.log(`\nDone: ${created} products synced to Stripe`);
console.log(`Output: src/lib/stripe-prices.ts`);
